/**
 * @param {number[][]} costs
 * @return {number}
 */
var minCost = function (costs) {
  let dp = costs[0];

  for (let i = 1; i < costs.length; i++) {
    dp = [
      Math.min(dp[1], dp[2]) + costs[i][0],
      Math.min(dp[0], dp[2]) + costs[i][1],
      Math.min(dp[0], dp[1]) + costs[i][2],
    ];
  }

  return Math.min(...dp);
};

console.log(minCost([[17, 2, 17], [16, 16, 5], [14, 3, 19]])) // 10
console.log(minCost([[7, 6, 2]])); // 2
console.log(minCost([[3, 5, 3], [6, 17, 6], [7, 13, 18], [9, 10, 18]])) // 26 = 3 + 6 + 7 + 10
console.log(minCost([[6, 17, 6], [7, 13, 18]])) // 13
